// ────────────────────────────────────────────────────────────────────────────
// Profession-first resolution helpers
// ────────────────────────────────────────────────────────────────────────────
// CRITICAL CONTRACT: every talent in TalentRank belongs to exactly ONE
// profession. The canonical answer is `professionId`. If a record only has the
// legacy `discipline` field, we resolve it through DISCIPLINE_TO_PROFESSION.
// All rankings, marquees, filters, and UI MUST go through these helpers so
// professions never get mixed.

/** Resolve the canonical profession id for a talent. Always returns a string —
 *  falls back to "other" for any record that can't be resolved. */
export function talentProfessionId(t: Talent): string {
  return t.professionId ?? DISCIPLINE_TO_PROFESSION[t.discipline] ?? "other";
}

/** Resolve the full Profession record for a talent. */
export function getTalentProfession(t: Talent): Profession {
  const id = talentProfessionId(t);
  return getProfession(id) ?? getProfession("other")!;
}

/** Strict filter: only talents whose canonical profession === `professionId`. */
export function getTalentsByProfession(professionId: string): Talent[] {
  return TALENTS.filter((t) => talentProfessionId(t) === professionId);
}

/** Top N talents within a single profession. Sorted by score desc. */
export function getTopByProfession(professionId: string, n: number = 10): Talent[] {
  return getTalentsByProfession(professionId)
    .sort((a, b) => b.score - a.score)
    .slice(0, n);
}

