  availabilityNote?: string;
  /** Sprint 2 vision: extended availability modes. */
  freelanceOnly?: boolean;
  remoteOnly?: boolean;
  availableInDays?: number;
  bio: string;
  tagline: string;
  showreelUrl?: string;
  links: { artstation?: string; vimeo?: string; youtube?: string; linkedin?: string; website?: string };
